    public boolean validaPeticion(){
        boolean paso=true;
        if(campoLugar.getText().equals("")){
            this.ErrorTipo=1;//error lugar
            paso=false;
        }
        if(campoFechaSalida.toString()==""){
            this.ErrorTipo=2;//error Fecha
            paso=false;
        }
        
         
         if(campoActividad.getText().equals("")){
            this.ErrorTipo=3;
            paso=false;
        }
         if(campoFechaSalida.getDate()==null){
              this.ErrorTipo=4;
              paso=false;
         }
        
         if(ComboPersonal.getSelectedItem().equals("Seleccione un empleado")){
             this.ErrorTipo=5;
             paso=false;
             
         }
         if(campoDias.getText().isEmpty()){
             this.ErrorTipo=6;
             paso=false;
             
         }if(campoDias.getText().equalsIgnoreCase("0")){
             this.ErrorTipo=7;
             paso=false;
         }
         if(seleccionadoE2.getText().equalsIgnoreCase("X") & tipo==1){
             this.ErrorTipo=8;
             paso=false;
         }
         
         
         return paso;
    }
    
    
    
    
    
    
    
    
    
