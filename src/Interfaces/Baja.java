    private void baja(){
        int idBaja = m.getMax(b.obtenerConsultas("select id_baja from baja"));
        String [] insertar = new String [3];
        insertar[0]=idBaja+"";
        insertar[1]=jTextArea1.getText();
        insertar[2]=idproducto;
        b.insertar("baja", insertar);
        b.execute("update producto set status_producto = 'Baja' where folio_producto = '"+idproducto+"'");
    }

