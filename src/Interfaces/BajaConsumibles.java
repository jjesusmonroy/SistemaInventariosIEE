    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if("".equals(jTextField1.getText())){
            JOptionPane.showMessageDialog(this,"Seleccionar cantidad a dar de baja");
            jTextField1.requestFocus();
            return;
        }
        if(v.soloNumeros(jTextField1.getText())){
            JOptionPane.showMessageDialog(this,"Solo se aceptan numeros");
            jTextField1.setText("");
            jTextField1.requestFocus();
            return;
        }
        int numerito = Integer.parseInt(stock)-Integer.parseInt(min);
        if(Integer.parseInt(jTextField1.getText())>(numerito)){
            JOptionPane.showMessageDialog(this,"Numero mayor a "+numerito);
            jTextField1.setText("");
            jTextField1.requestFocus();
            return;
        }
        b.execute("update producto set stock_producto ='"+(Integer.parseInt(stock)-Integer.parseInt(jTextField1.getText()))+"' where folio_producto = '"+folioproducto+"'");
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

